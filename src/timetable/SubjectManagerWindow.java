    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (cmbClasses.getSelectedIndex() >= 0) {
            String stdclass = listClasses.getElementAt(cmbClasses.getSelectedIndex());
            StudentClass cls = App.getClasses().get(App.getClasses().indexOf(stdclass));
            Subject s = new Subject(txtName.getText(), txtCode.getText(), cls, Integer.parseInt(spnSlots.getValue().toString()));

            if (!App.getSubjects().contains(s.toString())) {
                System.out.println("added subject" + s.getCode());
                App.getSubjects().add(s);
            }

            refreshList();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int index=jlistSubjects.getSelectedIndex();
        if (index >= 0) {
            String sub= listSubjects.get(index);
            
            App.getSubjects().remove(App.getSubjects().indexOf(sub));
            listSubjects.remove(index);
            
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

