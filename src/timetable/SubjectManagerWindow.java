    public void update() {
        listClasses.removeAllElements();

        List<StudentClass> list = App.getClasses();
        for (int i = 0; i < list.size(); i++) {
            listClasses.addElement(list.get(i).getName());
        }

    }

    public void refreshList() {

        listSubjects.clear();
        List<Subject> list = App.getSubjects();

        for (int i = 0; i < list.size(); i++) {
            listSubjects.addElement(list.get(i).toString());
        }

    }
